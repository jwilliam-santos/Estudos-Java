//Escreva um programa que faça uma contagem regressiva de 10 até 1 utilizando um loop for e, no final, exiba a mensagem "Decolar!".



public class ex1{
    public static void main(String[]args){
        for( int i=11;i>=1;i--){
            System.out.println(i);
        }
        System.out.println("Decolar");
    }
    
}
